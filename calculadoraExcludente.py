numero = int(input('Insira um número '))
outroNumero = int(input('Insira outro número '))
operacao = input('Insira a operação ')

if operacao == ('+'):
	print(numero + outroNumero)
if operacao == ('-'):
	print(numero - outroNumero)
if operacao == ('/'):
	print('Operação inválida!')
if operacao == ('*'):
	print('Operação inválida!')